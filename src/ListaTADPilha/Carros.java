package ListaTADPilha;

public class Carros {

    private String ES;
    private String placa;
    public Carros(){
        
    }
    public Carros(String es, String plc) {
        setES(es);
        setPlaca(plc);
    }

    public String getES() {
        return ES;
    }

    public void setES(String ES) {
        this.ES = ES;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }
    
    
    
    
    
    
    
}
